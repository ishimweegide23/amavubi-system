    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // TODO add your handling code here:
        if (!validateAllFields()) {
        return;
    }
    
    try {
        Fan newFan = new Fan();
        newFan.setNationalId(txtNationalId.getText().trim());
        newFan.setName(txtFullName.getText().trim());
        newFan.setEmail(txtEmail.getText().trim());
        newFan.setPhone(txtPhoneNumber.getText().trim());
        newFan.setPassword(new String(txtPassword.getPassword()));
        newFan.setTier(comboTier.getSelectedItem().toString());
        newFan.setRole("fan"); // Default role
        
        FanDAO fanDAO = new FanDAO();
        boolean success = fanDAO.registerFan(newFan);
        
        if (success) {
            JOptionPane.showMessageDialog(this, "Fan added successfully!", 
                "Success", JOptionPane.INFORMATION_MESSAGE);
            dispose(); // Close the dialog
        } else {
            JOptionPane.showMessageDialog(this, "Failed to add fan", 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), 
            "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnOKActionPerformed

