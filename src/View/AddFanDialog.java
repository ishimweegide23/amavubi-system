private void showError(String message, JTextField field) {
    JOptionPane.showMessageDialog(this, message,
        "Validation Error", JOptionPane.ERROR_MESSAGE);
    field.setBorder(BorderFactory.createCompoundBorder(
        BorderFactory.createEmptyBorder(8, 0, 8, 0),
        BorderFactory.createMatteBorder(0, 0, 2, 0, Color.RED)));
}

private void setValidBorder(JTextField field) {
    field.setBorder(BorderFactory.createCompoundBorder(
        BorderFactory.createEmptyBorder(8, 0, 8, 0),
        BorderFactory.createMatteBorder(0, 0, 2, 0, new Color(0, 102, 204))));
}

private boolean validateAllFields() {
    boolean isValid = true;
    
    // National ID validation
    String nationalId = txtNationalId.getText().trim();
    if (nationalId.isEmpty() || !nationalId.matches("^\\d{16}$")) {
        showError("National ID must be exactly 16 numeric digits", txtNationalId);
        isValid = false;
    }
    
    // Full name validation
    String name = txtFullName.getText().trim();
    String[] forbiddenLocationWords = {"rwanda", "burundi", "uganda", /* your list */};
    if (name.isEmpty() || name.length() > 30 || 
        !name.matches("^[a-zA-ZÀ-ÿ ]+$") || 
        name.split("\\s+").length > 3 ||
        Arrays.stream(forbiddenLocationWords).anyMatch(loc -> name.toLowerCase().contains(loc))) {
        showError("Invalid name format", txtFullName);
        isValid = false;
    }
    
    // Phone validation
    String phone = txtPhoneNumber.getText().trim();
    if (!phone.matches("^\\d{10}$")) {
        showError("Phone must be 10 digits (no spaces)", txtPhoneNumber);
        isValid = false;
    }
    
    // Email validation
    String email = txtEmail.getText().trim();
    if (email.isEmpty() || email.length() > 30 || 
        !email.matches("^[\\w-.]+@([\\w-]+\\.)+[\\w-]{2,4}$")) {
        showError("Invalid email format", txtEmail);
        isValid = false;
    }
    
    // Password validation
    String password = new String(txtPassword.getPassword()).trim();
    if (password.isEmpty() || password.length() != 6) {
        showError("Password must be exactly 6 characters", txtPassword);
        isValid = false;
    }
    
    return isValid;
}

