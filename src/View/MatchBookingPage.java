    private void confirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmButtonActionPerformed
        // TODO add your handling code here:
         boolean isValid = true;
    String errorMessage = "";
    JComponent errorComponent = null;
    
    // Check match selection
    if (matchComboBox.getSelectedItem() == null || matchComboBox.getSelectedIndex() == 0) {
        isValid = false;
        errorMessage = "Please select a match from the list";
        errorComponent = matchComboBox;
    }
    // Check seat category selection
    else if (seatCategoryComboBox.getSelectedItem() == null || seatCategoryComboBox.getSelectedIndex() == 0) {
        isValid = false;
        errorMessage = "Please select a seat category";
        errorComponent = seatCategoryComboBox;
    }
    // Check payment method selection
    else if (paymentMethodComboBox.getSelectedItem() == null || paymentMethodComboBox.getSelectedIndex() == 0) {
        isValid = false;
        errorMessage = "Please select a payment method";
        errorComponent = paymentMethodComboBox;
    }
    
    // If any validation failed, show error and return
    if (!isValid) {
        showError(errorMessage);
        if (errorComponent != null) {
            errorComponent.requestFocusInWindow();
        }
        return;
    }

    // All validations passed - proceed with booking
    String selectedMatch = (String) matchComboBox.getSelectedItem();
    String selectedSeatCategory = (String) seatCategoryComboBox.getSelectedItem();
    String selectedPaymentMethod = (String) paymentMethodComboBox.getSelectedItem();

    try {
        // Additional validation for match format
        if (!selectedMatch.contains(" on ")) {
            showError("Invalid match format. Please select a valid match.");
            return;
        }

        // Rest of your existing booking logic...
        String[] matchDetails = selectedMatch.split(" on ");
        String opponent = matchDetails[0].replace("Rwanda vs ", "").trim();
        String matchDate = matchDetails[1].trim();

        MatchDAO matchDAO = new MatchDAO();
        int matchId = matchDAO.getMatchIdByOpponentAndDate(opponent, matchDate);

        if (matchId == -1) {
            showError("Selected match is no longer available.");
            return;
        }

        double ticketPrice = matchDAO.getTicketPriceByCategory(matchId, selectedSeatCategory);

        if (!matchDAO.updateAvailableSeats(matchId, selectedSeatCategory)) {
            showError("No available seats in " + selectedSeatCategory + " category.");
            return;
        }

        TransactionDAO transactionDAO = new TransactionDAO();
        boolean success = transactionDAO.insertMatchBooking(
            fan.getFanId(), 
            matchId, 
            ticketPrice, 
            selectedPaymentMethod
        );

        if (success) {
            showSuccess(selectedMatch, selectedSeatCategory, selectedPaymentMethod, ticketPrice);
            resetForm();
        } else {
            showError("Booking failed. Please try again.");
        }
    } catch (Exception e) {
        showError("An error occurred: " + e.getMessage());
    }
    }//GEN-LAST:event_confirmButtonActionPerformed

