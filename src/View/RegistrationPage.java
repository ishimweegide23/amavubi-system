    private void showError(String message, JComponent field) {
        JOptionPane.showMessageDialog(this, message, "Validation Error", JOptionPane.ERROR_MESSAGE);
        if (field instanceof JTextField) {
            ((JTextField)field).setBorder(BorderFactory.createLineBorder(Color.RED));
        } else if (field instanceof JPasswordField) {
            ((JPasswordField)field).setBorder(BorderFactory.createLineBorder(Color.RED));
        }
    }

private void setValidBorder(JTextField field) {
    field.setBorder(BorderFactory.createCompoundBorder(
        BorderFactory.createEmptyBorder(8, 0, 8, 0),
        BorderFactory.createMatteBorder(0, 0, 2, 0, new Color(0, 102, 204))));
    }//GEN-LAST:event_nationalIdFieldActionPerformed

    private void nameFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nameFieldActionPerformed
        // TODO add your handling code here:
        String name = nameField.getText().trim();
    
    // Comprehensive list of forbidden location words (countries, cities, districts)
    String[] forbiddenLocationWords = {
        // Countries
        "rwanda","burundi","uganda","tanzania","kenya","congo",
        // Cities
        "kigali","butare","gisenyi","ruhengeri","byumba","cyangugu","kibuye","kibungo",
        "musanze","rubavu","nyanza","huye","burera","nyagatare","rwamagana","nyamagabe",
        // Districts
        "gasabo","nyarugenge","kicukiro","bugesera","gatsibo","kayonza","ngoma","nyaruguru",
        "gisagara","kamonyi","muhanga","nyamagabe","nyanza","ruhango","rusizi","rutsiro",
        "karongi","nyabihu","nyamasheke","rubavu","rulindo"
    };

    if (name.isEmpty()) {
        showError("Full name cannot be empty", nameField);
    } 
    else if (name.length() > 30) {
        showError("Name cannot exceed 30 characters", nameField);
    }
    else if (!name.matches("^[a-zA-ZÀ-ÿ ]+$")) { // Only letters (including accents) and spaces
        showError("Name must contain only letters (no numbers or symbols)", nameField);
    }
    else if (name.split("\\s+").length > 3) { // Maximum 3 words
        showError("Name should contain maximum 3 words", nameField);
    }
    else if (Arrays.stream(forbiddenLocationWords)
                 .anyMatch(loc -> name.toLowerCase().contains(loc))) {
        showError("Name cannot contain location names (cities/countries)", nameField);
    }
    else if (name.matches(".*\\s{2,}.*")) { // No multiple spaces
        showError("Remove extra spaces between names", nameField);
    }
    else {
        setValidBorder(nameField);
    }
    }//GEN-LAST:event_nameFieldActionPerformed

