    private void emailFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_emailFieldActionPerformed
        // TODO add your handling code here:
       String email = emailField.getText().trim();
    
    if (email.isEmpty()) {
        showError("Email cannot be empty", emailField);
    }
    else if (email.length() > 30) {
        showError("Email cannot exceed 30 characters", emailField);
    }
    else if (email.split("@")[0].split("[\\._]").length > 3) { // Before @
        showError("Email prefix should contain maximum 3 words", emailField);
    }
    else if (!email.matches("^[\\w-.]+@([\\w-]+\\.)+[\\w-]{2,4}$")) {
        showError("Invalid email format (example: user@domain.com)", emailField);
    }
    else {
        setValidBorder(emailField);
    }
    }//GEN-LAST:event_emailFieldActionPerformed

