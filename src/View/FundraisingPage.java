    private void donateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_donateButtonActionPerformed
        // TODO add your handling code here:
        String amountText = amountField.getText().replaceAll("[\\s\\u00A0]", "");
    String paymentMethod = (String) paymentMethodBox.getSelectedItem();

    // Validation: Empty amount
    if (amountText.isEmpty()) {
        JOptionPane.showMessageDialog(this, 
            "Please enter the donation amount", 
            "Amount Required", 
            JOptionPane.WARNING_MESSAGE);
        amountField.requestFocus();
        return;
    }

    try {
        // Clean and parse amount
        amountText = amountText.replace(",", ".");
        double amount = Double.parseDouble(amountText);
        
        // Validation: Negative or zero amount
        if (amount <= 0) {
            JOptionPane.showMessageDialog(this, 
                "Amount must be greater than zero", 
                "Invalid Amount", 
                JOptionPane.WARNING_MESSAGE);
            amountField.selectAll();
            amountField.requestFocus();
            return;
        }

        // Process donation
        TransactionDAO dao = new TransactionDAO();
        boolean success = dao.insertFundraisingTransaction(fanId, amount, "fundraising");

        if (success) {
            JOptionPane.showMessageDialog(this,
                "<html><b>Thank you for your donation!</b><br>" +
                "Your RWF " + String.format("%,.0f", amount) + " helps Amavubi grow stronger!</html>",
                "Donation Successful",
                JOptionPane.INFORMATION_MESSAGE);
            amountField.setText("");
        } else {
            JOptionPane.showMessageDialog(this,
                "Failed to process donation. Please try again.",
                "Donation Error",
                JOptionPane.ERROR_MESSAGE);
        }

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, 
            "Please enter a valid amount (e.g., 10000 or 10,000)", 
            "Invalid Amount Format", 
            JOptionPane.WARNING_MESSAGE);
        amountField.selectAll();
        amountField.requestFocus();
    }
    }//GEN-LAST:event_donateButtonActionPerformed

