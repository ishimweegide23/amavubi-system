     private void loadPlayerData() {
    try {
        PlayerDAO playerDAO = new PlayerDAO();
        List<Player> players = playerDAO.getAllPlayers();

        tableModel.setRowCount(0);

        if (players != null && !players.isEmpty()) {
            for (Player player : players) {
                Object[] rowData = {
                    player.getPlayerId(),
                    player.getName(),
                    player.getPosition(),
                    player.getJerseyNumber(),
                    player.getDateOfBirth(),
                    player.getNationality(),
                    player.isActive(),
                    player.getGoals(),
                    player.getAssists(),
                    player.getMatchesPlayed()
                };
                tableModel.addRow(rowData);
            }
        } else {
            JOptionPane.showMessageDialog(this, 
                "No player data available", 
                "Information", 
                JOptionPane.INFORMATION_MESSAGE);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, 
            "Error loading player data: " + e.getMessage(), 
            "Error", 
            JOptionPane.ERROR_MESSAGE);
    }
}

private void searchPlayer() {
    String searchTerm = searchField.getText().trim();
    if (searchTerm.isEmpty()) {
        JOptionPane.showMessageDialog(this, 
            "Please enter a player name to search", 
            "Search Error", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }

    String[] retiredPlayers = {
        "Haruna Niyonzima",
        "Olivier Karekezi",
        "Jimmy Mulisa",
        "Jean Lomami"
    };

    for (String retiredPlayer : retiredPlayers) {
        if (retiredPlayer.toLowerCase().contains(searchTerm.toLowerCase())) {
            JOptionPane.showMessageDialog(this, 
                retiredPlayer + " is a retired Amavubi legend", 
                "Retired Player", 
                JOptionPane.INFORMATION_MESSAGE);
        }
    }

    try {
        PlayerDAO playerDAO = new PlayerDAO();
        List<Player> players = playerDAO.searchPlayersByName(searchTerm);

        tableModel.setRowCount(0);

        if (players != null && !players.isEmpty()) {
            for (Player player : players) {
                Object[] rowData = {
                    player.getPlayerId(),
                    player.getName(),
                    player.getPosition(),
                    player.getJerseyNumber(),
                    player.getDateOfBirth(),
                    player.getNationality(),
                    player.isActive(),
                    player.getGoals(),
                    player.getAssists(),
                    player.getMatchesPlayed()
                };
                tableModel.addRow(rowData);
            }
        } else {
            JOptionPane.showMessageDialog(this, 
                "No current players found matching: " + searchTerm, 
                "Player Not Found", 
                JOptionPane.INFORMATION_MESSAGE);
            loadPlayerData();
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, 
            "Error searching player data: " + e.getMessage(), 
            "Error", 
            JOptionPane.ERROR_MESSAGE);
    }
}
