      private void setupTableModel() {
    String[] columnNames = {"ID", "Player Name", "Position", "Jersey No.", "Date of Birth", 
                          "Nationality", "Active", "Goals", "Assists", "Matches"};
    tableModel = new DefaultTableModel(columnNames, 0) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false;
        }
        @Override
        public Class<?> getColumnClass(int columnIndex) {
            return columnIndex == 6 ? Boolean.class : Object.class;
        }
    };
    table.setModel(tableModel);
    styleTable(table);
}
      private void styleTable(JTable table) {
    table.setFont(new Font("Segoe UI", Font.PLAIN, 14));
    table.setRowHeight(35);
    table.setSelectionBackground(new Color(0, 75, 14));
    table.setSelectionForeground(Color.WHITE);
    table.setShowGrid(false);
    table.setIntercellSpacing(new Dimension(0, 0));
    table.setFillsViewportHeight(true);

    JTableHeader header = table.getTableHeader();
    header.setFont(new Font("Segoe UI", Font.BOLD, 14));
    header.setBackground(new Color(32, 136, 203));
  
    header.setForeground(Color.WHITE);
    header.setReorderingAllowed(false);

    table.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
        @Override
        public Component getTableCellRendererComponent(JTable table, Object value,
                boolean isSelected, boolean hasFocus, int row, int column) {
            Component c = super.getTableCellRendererComponent(table, value, isSelected, 
                    hasFocus, row, column);
            c.setForeground(Color.BLACK);
            if (!isSelected) {
                c.setBackground(row % 2 == 0 ? Color.WHITE : new Color(240, 240, 240));
            }
            setBorder(BorderFactory.createEmptyBorder(0, 10, 0, 10));
            setHorizontalAlignment(column == 3 || column >= 6 ? CENTER : LEFT);
            return c;
        }
    });
}

